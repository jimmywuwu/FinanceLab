########################(4) dataframe 例子 #############################
########################################################################

  ###1. dataframe 造法
  
    a4 <- data.frame(
      x = letters[1:6],
      y = rnorm(6),
      z = runif(6) > 0.5
    )
    
    b4= data.frame(letters[1:6],rnorm(6),runif(6) > 0.5)
    colnames(b4)=c('x','y','z')
    rownames(b4)=c('row1','row2','row3','row4','row5','row6')
    
  ###2. dataframe 取值
  
    b4[1] #拿出第一列
    b4[2:4,] #拿出2~4行
    b4$x # $取列
    
  ###3. dataframe 運算方法  
  
    as.matrix(b4[,2]) #基本上如果要運算就使用as.matrix轉換成矩陣做運算
    as.matrix(b4$x)
    as.matrix(b4)
    
  ###4. dataframe 特色
    
    #** dataframe 使用上與矩陣十分相似，以下列舉幾個dataframe的特點
    #** dataframe 裡面可以容納各種不同的資料型態（矩陣、陣列、向量無法做到）
    #** dataframe 在取columns的時候如果有給定表頭，可以使用$取值
    

