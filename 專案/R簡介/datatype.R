#########################(1) vector 例子#########################
#################################################################

  ### 1.vector造法
    a1=c(2,1,3,1)
    b1=1:4
    c1=seq(1,8,3)
    d1=c(a1,b1)
    e1=c(d1,"a") #字串可以跟數字向量合在一起，但是會把其他資料都變成字串
    
  ### 2.vector 取值
    a1[1]
    a1[2]
  
  ### 3.vector 運算
    a1*b1   #各分量相乘
    a1%*%b1 #向量內積計算
    a1^4    #各分量4次方
    2*a1+1  #個分量全部*2+1
  
  ### 4. vector 特色
    #(i)向量可以直接放在 for 迴圈判斷式迭代
      d1
      for(i in 1:length(d1)){
        print(d1[i])
      }
      
      e1
      for(i in e1){
        print(i)
      }
    #(ii) 長度資訊
      length(e1)

########################(2) matrix 例子 #############################
######################################################################

  ###1. 矩陣造法
    #(i) 一般取法
      a2=matrix(1:9,3,3)
      a2
    
    #(ii) rbind,cbind
      b2=c(2,1,3,1)
      c2=1:4
      d2=c("a","b","c","d")
      
      cbind(a2,b2)
      rbind(a2,b2)
    
      cbind(b2,c2,d2) # 與vector一樣只能存一種資料格式（兩者都是array的特例）
      
  ###2. 矩陣取值
      e2=a2
      e2[3,1] #(3,1)位置
      e2[,1]  #拿出第一列
      e2[1,]  #拿出第一行
      e2[1,1:2] #拿出第一行,第一列到第二列
      e2[1:2,1:2] 
  
  ###3. 矩陣運算
      e2
      f2=matrix(1:3,3,1)
      
      2*e2+1 # 
      e2%*%f2# 矩陣乘法
    
  
  ###4. 矩陣特色
    #(i). 行列命名
    
      g2=a2
      colnames(g2)=c("a","b","c")
      rownames(g2)=c("d","e","f")
      g2
    
    #(ii) 可以放在for 迴圈內迭代
      for(i in g2){
        print(i)
      }
    
    #(iii) 維度資訊
      length(g2)
      nrow(g2)
      ncol(g2)
      dim(g2)
    

